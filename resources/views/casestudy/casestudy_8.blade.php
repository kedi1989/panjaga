              <h2><strong>Czyszczenie wózków dziecięcych. Czyszczenie stelaża, pranie gondoli wózka - Karcher - Leszno</strong></h2>

              <p>Stelaż wózka został doczyszczony za pomocą parownicy KARCHER SV-7. Mikrofibra z gorącą parą w gruncie rzeczy załatwia sprawę dość szybko a stary brud schodzi dużo łatwiej. Czyszczenie parowe ma tą zaletę, że odbywa się bezchemicznie i jest bardzo bezpieczne, nie ma tu ryzyka jakiegoś odbarwienia czy czegokowiek w tym stylu.</p>

              <h2><strong>Pranie i czyszczenie gondoli wózka dziecięcego - Usługi karcher - Leszno</strong></h2>

              <p>Jeżeli chodzi o gondolę wózka naszej klientki to standardowo był on prany ekstrakcyjnie odkurzaczem piorącym. Po zlaniu wózka ciepłą wodą z chemią, tapicerka wózka została przetarta szczotką z miękkim włosiem- z miękkim dlatego, że tapicerka wózka dziecięcego jest dość delikatna i gdybym zrobił to twardą to mógłbym zmechacić materiał.</p>

              <p>Tak więc tapicerka została wyprana ekstrakcyjne po czym przepłukana wodą aby usunąć pozostałości chemii a cała gondola jeszcze przed tym całym procesem została odkażona parownicą- gorąca para zabija roztocza i różne tego typu nieprzyjemne rzeczy.</p>

