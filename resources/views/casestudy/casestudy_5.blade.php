              <h2><strong>Pranie i czyszczenie wózka dziecięcego - Jaka chemia ? - Śrem</strong></h2>

              <p>To ważne pytanie bo zazwyczaj klientki albo klienci chcący zaspokoić wiedzę swojej żony pytają. Jakiej chemii Pan używa i czy jest to bezpieczne ?</p>

              <p>Wielu z was wydaje się, że pranie tapicerki to jakaś inwazja, mocna chemia itd. Nie. Równie dobrze, jak udałoby się wam włożyć wózek do pralki to też podejrzewam że byście go doprali, ale proszę Państwa- wózków dziecięcych, tapczanów, samochodów, samochodów cieżarowych itd. Do pralki nie wkładamy :) dzwonimy po PanaJagę i pierzemy to odkurzaczem piorącym ekstrakcyjnie :) I piszę to całkiem serio.</p>

              <p>Chemia użyta to Karcher RM760, parownica i woda z płukanką do wypłukania resztek brudu i pozostałości chemii. To wszystko. Czysto i bezpiecznie.</p>

              <p>Tak więc zapraszam. Mogę dojechać, można mi wózek dowieźć.</p>
              

