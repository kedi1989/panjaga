              <h2><strong>Czyszczenie mebli - Słów kilka - Śrem</strong></h2>

              <p>Pranie mebli to nie jest jakaś inwazja :) tylko 1,5 - 3 godzinna praca w zależności od nakładu pracy, wielkości itd. Nie należy bać się, że meble będą mokre kilka dni albo, że coś spleśnieje. Nic z tych rzeczy.</p>

              <p>Po wyciągnięciu wody z brudem z tapicerki mebli, pozostaje tylko wilgoć resztkowa. Odbywa się to pod dużym ciśnieniem odkurzacza piroącego więc mamy pewność, że nie wiele wody zostanie w naszym meblu. Tapicerka zazwyczaj schnie w ciągu 10 godzin a nawet i szybciej.</p>

